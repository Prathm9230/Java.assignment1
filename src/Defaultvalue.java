public class Defaultvalue {
    public static boolean a;
    public static byte defByte;
    public static short defShort;
    public static int defInt;
    public static long defLong;
    public static float defFloat;
    public static double defDouble;
    public static char defChar;
    public static void main(String[] args)
    {
        System.out.println(" -- Default values of primitive data types are --:");
        System.out.println("Default boolean value is:"+ a);
        System.out.println("Default byte value is:"+ defByte);
        System.out.println("Default short value is:"+defShort);
        System.out.println("Default int value is:"+ defInt);
        System.out.println("Default long value is:"+ defLong);
        System.out.println("Default float value is:"+ defFloat);
        System.out.println("Default double value is:"+ defDouble);
        System.out.println("Default char value is:"+ defChar);
    }
}
