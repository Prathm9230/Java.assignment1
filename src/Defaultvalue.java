public class Defaultvalue {
    public static boolean a;
    public static byte defByte;
    public static short Short;
    public static int Int;
    public static long Long;
    public static float Float;
    public static double Double;
    public static char Char;
    public static void main(String[] args)
    {
        System.out.println(" -- Default values of primitive data types are --:");
        System.out.println("Default boolean value is:"+ a);
        System.out.println("Default byte value is:"+ defByte);
        System.out.println("Default short value is:"+ Short);
        System.out.println("Default int value is:"+ Int);
        System.out.println("Default long value is:"+ Long);
        System.out.println("Default float value is:"+ Float);
        System.out.println("Default double value is:"+ Double);
        System.out.println("Default char value is:"+ Char);
    }
}
